func TestRenderClaimsContractTS(t *testing.T) {
	s := mustSpec(t, runtimeSpec)
	got, err := s.RenderClaimsContractTS("Contract")
	if err != nil {
		t.Fatalf("RenderClaimsContractTS: %v", err)
	}
	want := "// Code generated by Demesne. DO NOT EDIT.\n" +
		`export const Contract: string[] = ["customer_id","project_id","sub","tenant_id"];` + "\n"
	if got != want {
		t.Errorf("RenderClaimsContractTS:\n got: %q\nwant: %q", got, want)
	}
}

