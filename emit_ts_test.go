func TestEmitTS_ReadSurfacesOnly(t *testing.T) {
	s := mustSpec(t, runtimeSpec)
	got, err := s.EmitTS()
	if err != nil {
		t.Fatalf("EmitTS: %v", err)
	}
	mustContain(t, got, []string{
		`import type { AppObjectSurface, Claims, Pdp } from "@demesne/runtime";`,
		"export const claims: Claims = {",
		`"setting": "request.jwt.claims"`,
		`"role": "authenticated"`,
		`"key": "customer_id"`,
		"export const appSurface: AppObjectSurface[] = [",
		`"table": "records"`,
		"export const pdp: Record<string, Pdp> = {",
		`"records.v1.RecordsService/UpdateRecord": "content:write"`,
	})
	for _, absent := range []string{"holdsResolver", "roleAssignment", "export const grants", "resourceAccess"} {
		if strings.Contains(got, absent) {
			t.Errorf("EmitTS(read-only) should not emit %q:\n%s", absent, got)
		}
	}
}

